var BigNumber = require('../../src/numbers/BigNumber');

test('test big number division', () => {

    let a = new BigNumber('189620700613125325959116839007395234454467716598457179234021');
    let b = new BigNumber('282174488599599500573849980909');
    let c = a.divide(b);
    expect(c.toString()).toBe('671998030559713968361666935769');

    a = new BigNumber('4911896143611633780682402453849989800775189204252065726408220729093666935169295333354972115958561596541882926741138174142872698595829944178957090296779606642580976458630868510407440881709556568052691');
    b = new BigNumber('2367495770217142995264827948666809233066409497699870112003149352380375124855230068487109373226251983');
    c = a.divide(b);
    expect(c.toString()).toBe('2074722246773485207821695222107608587480996474721117292752992589912196684750549658310084416732550077');

    a = new BigNumber('4911896143611633780682402');
    b = new BigNumber('236749577021714299526482794866680923306640');
    c = a.divide(b);
    expect(c.toString()).toBe('0.00000000000000002074722246773485207');

    a = new BigNumber('236749577021714299526482794866680923306640');
    b = new BigNumber('2');
    c = a.divide(b);
    expect(c.toString()).toBe('118374788510857149763241397433340461653320');

    a = new BigNumber('236749577021714299526482794866680923306640');
    b = new BigNumber('0.02');
    c = a.divide(b);
    expect(c.toString()).toBe('11837478851085714976324139743334046165332000');
})